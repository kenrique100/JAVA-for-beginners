package org.example.operations;

public class Relational {
    public static void main(String[] args) {
        int x = 1;
        int y = 2;

        if(x == y) {
            System.out.println("x == y");
        }
        if(x != y) {
            System.out.println("x != y");
        }
        if(x > y) {
            System.out.println("x > y");
        }
        if(x < y) {
            System.out.println("x < y");
        }
        if(x >= y) {
            System.out.println("x >= y");
        }
        if(x <= y) {
            System.out.println("x <= y");
        }
    }
}
